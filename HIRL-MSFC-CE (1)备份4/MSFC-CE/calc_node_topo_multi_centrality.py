    # 2. 创建图
    G = nx.Graph(topology)
    n = topology.shape[0]

    # 获取节点的度 (确保按照节点 index 顺序排列)
    # G.degree() 返回 (node, degree) 元组，我们提取 degree
    degree_dict = dict(G.degree())
    degrees = np.array([degree_dict[i] for i in range(n)])

    # 3. 初始化记录向量
    node_counts = np.zeros(n)
    steps = 17

    # 4. 遍历每个节点作为源节点
    for source in range(n):
        visited = np.zeros(n)
        current_set = [source]  # 对应 MATLAB: current_node = source_node

        for _ in range(steps):
            next_set = []  # 对应 MATLAB: temp_node = []

            # 遍历当前集合中的每个节点
            for node in current_set:
                # 获取邻居节点
                neighbors = list(G.neighbors(node))

                if not neighbors:
                    continue

                # 根据度数降序排序，取前2个
                # 注意：Python sorted 是稳定的，逻辑与 MATLAB sort 一致
                sorted_nbrs = sorted(neighbors, key=lambda x: degrees[x], reverse=True)[:2]

                for nbr in sorted_nbrs:
                    visited[nbr] += 1
                    next_set.append(nbr)

            # 更新当前节点集合
            current_set = next_set

        # 累加到总次数
        node_counts += visited

    # 5. 计算权重 (归一化)
    weight = node_counts / node_counts.sum()

    return weight


