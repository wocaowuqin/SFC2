    # create_using=nx.DiGraph 确保创建的是有向图
    G = nx.from_numpy_array(topology, create_using=nx.DiGraph)

    # 计算 PageRank
    # MATLAB 的 centrality(G, 'pagerank') 默认 damping factor (alpha) 通常为 0.85
    # NetworkX 的 nx.pagerank 默认 alpha 也是 0.85
